<template>
  <div class="b-controls">
    <span class="b-controls__title">
      {{ count }} users selected
    </span>
    <div
      v-if="showBtnGroup"
      class="b-controls__btns"
    >
      <el-button
        v-if="!disableEditBtn"
        class="b-btn b-btn_secondary b-btn_medium b-btn_icon"
        v-html="'<span>' + iconEdit + 'Edit' + '</span>'"
      >
      </el-button>
      <el-button
        class="b-btn b-btn_secondary b-btn_medium b-btn_icon"
        v-html="'<span>' + iconDelete + 'Delete' + '</span>'"
      >
      </el-button>
    </div>
    <el-button
      v-if="!!roleCode"
      class="b-btn b-btn_tag b-btn_icon"
      :class="tagClassModifier"
      @click="clearRoleFilter"
    >
      {{ roleName }}
      <el-icon>
        <CircleClose />
      </el-icon>
    </el-button>
  </div>
</template>
<script setup>
import { computed } from 'vue';
import { ElButton, ElIcon } from 'element-plus';
import { CircleClose } from '@element-plus/icons-vue';
import { getIcon } from '@/lib/template';
import { TagAccountListModifier } from '@/lib/constants';
import SelectRoleModel from '@/models/SelectRoleModel';

const { count, disableEditBtn, roleCode, roleName } = defineProps({
  count: {
    type: Number,
    default: 0
  },
  disableEditBtn: {
    type: Boolean,
    default: false
  },
  roleCode: {
    type: String,
    default: ''
  },
  roleName: {
    type: String,
    default: ''
  }
});

const emit = defineEmits(['clear-role']);

/**
 * @type {String}
 */
const iconDelete = computed(() => getIcon('delete'));
/**
 * @type {String}
 */
const iconEdit = computed(() => getIcon('edit'));
/**
 * @type {Number}
 */
const showBtnGroup = computed(() => {
  return count > 0;
});
/**
 * @type {String}
 */
 const tagClassModifier = computed(() => {
  return `${TagAccountListModifier}${roleCode}`
});

const clearRoleFilter = () => {
  emit('clear-role', new SelectRoleModel({
    roleCode: '',
    roleName: ''
  }));
}
</script>
